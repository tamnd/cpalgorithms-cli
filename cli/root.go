// Package cli builds the cpa command tree on top of the cpalgorithms library.
package cli

import (
	"github.com/spf13/cobra"
)

// Build metadata, set via -ldflags at release time.
var (
	Version = "dev"
	Commit  = "none"
	Date    = "unknown"
)

// Root builds the root command and its subtree.
func Root() *cobra.Command {
	root := &cobra.Command{
		Use:   "cpa",
		Short: "Browse CP-Algorithms competitive programming articles",
		Long: `Browse CP-Algorithms competitive programming articles

This is a fresh scaffold. Add your commands here on top of the cpalgorithms
library package, then wire them into Root with root.AddCommand.`,
		SilenceUsage:  true,
		SilenceErrors: true,
	}

	root.AddCommand(newVersionCmd())
	// TODO: root.AddCommand(newGetCmd()), etc.
	return root
}
